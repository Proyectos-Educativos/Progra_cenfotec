#Descripción:Determinar si una persona tiene fiebre. Una persona tiene fiebre si la temperatura es mayor a 37 grados. El programa recibe como información la temperatura de la persona.
fiebre= float(input("Ingrese su temperatura en grado Celsius:"))
if fiebre> 37:
        print("Tiene fiebre")
else : print ("No tiene fiebre")
